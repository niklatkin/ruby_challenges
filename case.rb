weather = 'sunny'
case weather
  when 'sunny'
    puts "Yay, it's Sunny! It's a perfect day for a bike ride."
  when 'raining'
    puts "Oh well. Good thing you have a rain cape."
  when 'snowing'
    puts "It's OK to leave the bike at home once in awhile."
  when 'foggy'
    puts "Better make sure your lights are fully charged."
end
